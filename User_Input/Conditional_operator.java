package User_Input;

public class Conditional_operator {

	public static void main(String[] args) 
	{
		int a=20;
		int b=30;
		int c=40;
		
		if(a>b)//false
		{
			System.out.println("1");
		}
		if(b<c)//false
		{
			System.out.println("2");
		}
		if(a<=b)//true
		{
			System.out.println("3");
		}
		if(a==b)
		{
			System.out.println("4");
		}
		if(a!=c)
		{
			System.out.println("5");
		}

	}

}
